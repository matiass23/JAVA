package tiposDatos;

public class FloatDoublePrimitive {
    public static void main(String[] args) {
        
        //float tiene 32 bits
        float miMinimoValorFloat = Float.MIN_VALUE;
        float miMaximoValorFloat = Float.MAX_VALUE;

        System.out.println("Float tiene valor MINIMO: "+ miMinimoValorFloat);
        System.out.println("Float tiene valor MAXIMO: "+ miMaximoValorFloat);


        //double tiene 64 bits
        double miMinimoValorDouble = Double.MIN_VALUE;
        double miMaximoValorDouble = Double.MAX_VALUE;

        System.out.println("Double tiene valor MINIMO: "+ miMinimoValorDouble);
        System.out.println("Double tiene valor MAXIMO: "+ miMaximoValorDouble);
    }
}
